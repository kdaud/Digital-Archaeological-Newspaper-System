    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed

        String uname = username.getText();
        String pword = password.getText();
        if (uname.isEmpty() || pword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please make sure to enter your username and password");    //Show an alert

        } else if (uname.equals(SYSTEM_USER_NAME) && pword.equals(SYSTEM_PASSWORD)) {
            // Show main window
            try {
                MainGUI obj = new MainGUI();
                obj.show();
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);

            }

        } else {
            JOptionPane.showMessageDialog(null, "Authentication Failure");  // show an alert message
        }

    }//GEN-LAST:event_LoginActionPerformed

